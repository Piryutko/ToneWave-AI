function getRandomElement<T>(array: T[]): T {
    return array[Math.floor(Math.random() * array.length)];
}

export async function* simulateStreamingResponse(text: string, mode: TransformMode) {
    const lowerText = text.toLowerCase().trim();
    let baseResponse = "";

    // Check for specific mappings first
    if (RESPONSE_MAPPING[lowerText] && RESPONSE_MAPPING[lowerText][mode]) {
        baseResponse = RESPONSE_MAPPING[lowerText][mode];
    } else {
        baseResponse = getRandomElement(PERSPECTIVES[mode]);
    }

    const suffixes = [
        " Это существенно повысит качество коммуникации.",
        " С нетерпением жду ваших комментариев по этому поводу.",
        " Надеюсь на продуктивное сотрудничество!",
        " Давайте двигаться в этом направлении.",
        " Готов детально проработать данный вопрос."
    ];
    
    // In LACONIC mode or for direct mappings we don't always add suffixes
    const needsSuffix = mode !== 'LACONIC' && !RESPONSE_MAPPING[lowerText];
    const fullText = needsSuffix ? baseResponse + (text.length > 5 ? getRandomElement(suffixes) : "") : baseResponse;
    
    // Simulate streaming by breaking text into chunks
    const words = fullText.split(' ');
    for (const word of words) {
        // Random delay between 50 and 150ms
        await new Promise(resolve => setTimeout(resolve, 50 + Math.random() * 100));
        yield word + ' ';
    }
}
